package News::Pan;

1;

=pod

=head1 NAME

News::Pan

=head1 DESCRIPTION

This is a collection of routines to interact with your Pan newsreader cache.
Pan is great, I love it. But it can suck searching through a million articles in a million groups. So, here's some scripting to do it.

=head1 API

=head2 L<News::Pan::Server>

Getting list of groups cached.. etc.

=head2 L<News::Pan::Group>

Getting subjects of articles, etc.

=head3 L<News::Pan::Group::_Search>

Searching one newsgroup.

=head1 CLI

Bringing it all together..

=head2 L<panfind>

Included in this distro.
search all newsgroups at once for articles that match, tells you count and newsgroup it's in, etc.
Really cool.

